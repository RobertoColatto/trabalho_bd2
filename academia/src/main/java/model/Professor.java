package model;

/**
 *
 * @author Roberto Augusto
 */
public class Professor {
    
    private int id;
    private String nome;
    private char sexo;
    private String cref;
    private String telefone;
    private String email;
    
    public Professor() {}
    
    public Professor(String nome, char sexo, String cref, String telefone, String email) {
        this.nome = nome;
        this.sexo = sexo;
        this.cref = cref;
        this.telefone = telefone;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getCref() {
        return cref;
    }

    public void setCref(String cref) {
        this.cref = cref;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return id + " - " + nome + " (CREF: " + cref + ")";
    }
}
